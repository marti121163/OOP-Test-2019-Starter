package ie.tudublin;

public class Colour {
    private String colour;
    public int r;
    public int g;
    public int b;
    public int value;


    // constructor
    public Colour (String colour, int r, int g , int b, int value){
        this.colour = colour;
        this.r = r;
        this.g = g;
        this.b = b;
        this.value = value;
    }
}
