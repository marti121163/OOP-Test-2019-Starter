package ie.tudublin;

public class Resistor {

    public int value;
    public int ones;
    public int tens;
    public int hundreds;

    // constructor
    public Resistor(int value, int ones, int tens, int hundreds) {
        this.value = value;
        this.ones = ones;
        this.tens = tens;
        this.hundreds = hundreds;
    }
}
